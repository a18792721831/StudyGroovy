class Loop {

    static void main(String[] args){

        def len = 10
        println('len')
        for(i in len){
            println(i)
        }
        def lens = [0,1,2,3,4,5,6,7,8,8]
        println('lens')
        for(i in lens){
            println(i)
        }
        def str = 'hello world'
        println(str)
        for(i in str){
            println(i)
        }
    }

}
